def write2file(path, info, output):
    filereader = open(path, "w")
    for sample_idx, sample in enumerate(output):
        for word_idx, word in enumerate(sample):
            filereader.writelines(
                "{} 1 {:.2f} {:.2f} {}\n".format(
                    info[sample_idx],
                    word_idx * 1.0 / 100,
                    (word_idx + 1) * 1.0 / 100,
                    word[0]
                )
            )